def get_role_prompt(role: str, file_count: int, content: str) -> Optional[str]:
    """指定ロールのプロンプトを組み立てて返す。未知のロールは None を返す。"""
    template = ROLE_PROMPTS.get(role)
    if template is None:
        return None
    return template.format(file_count=file_count, content=content)
